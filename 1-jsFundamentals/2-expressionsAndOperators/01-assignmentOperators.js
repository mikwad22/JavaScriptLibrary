//assignement operator
let r = 5;

//addition assignment operator
r = r + 1;
r += 1; //shorthand for above

//subtraction assignment
r = r - 1;
r -= 1;

//multiplication assignment
r = r * 1;
r *= 1;

//division assignment
r = r / 1;
r /= 1;

//modulus assignment or remainder
r = r % 1;
r %= 1;

//exponential assignment
r = r ** 1; //multipies by itself
r **= 1;