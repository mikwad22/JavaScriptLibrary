console.log("3" == 3);  //TRUE: cohersion, two equal signs, does not check for data type, just value.

//strict equal
console.log("3" === 3); //FALSE: three equal signs, strict equal DOES check for data type

//not equal
console.log("3" != 3); //false

//strict not equal
console.log("3" !== 3); //true

//greater than
console.log(3 > 1); //true

//less than
console.log(3 < 1); //false

//greater than or equal to
console.log(5 >= 3); //true

//less than or equal to
console.log(5 <= 4); //false

//and
console.log(1 > 0 && 2 >1); //true, if either one was false the whole thing will turn false. both need to be true.

//or
console.log(1 > 0 || 2 >7) //true, only one needs to be true

